function InsertSort2(&$a, $n) {
	for ($i = 1; $i < $n; $i++) {
		$x = $a[$i];
		$j = $i - 1;
		while ($x < $a[$j]) {  //查找在有序表的插入位置
			$a[$j + 1] = $a[$j];
			$a[$j] = $x;
			$j--;
		}
	}
}

